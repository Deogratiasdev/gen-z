// Fonction pour obtenir 10 questions aléatoires selon la difficulté
List<Question> getRandomQuestions(String difficulty, {int count = 10}) {
  List<Question> source;
  switch (difficulty) {
    case 'facile':
      source = questionsFacile;
      break;
    case 'moyen':
      source = questionsMoyen;
      break;
    case 'difficile':
      source = questionsDifficile;
      break;
    default:
      source = questionsMoyen;
  }

  final random = Random();
  final shuffled = List<Question>.from(source)..shuffle(random);
  return shuffled.take(count).toList();
}

